public class SolveEquation {
    public static void main(String[] args) {
        int A = 5;
        int B = 2;
        int C = 7;
        int D = 8;
        int a = A*B-C*D;
        int b = 2*A-B+3*D;
        int c = A*A+B*B-C*C+D*D;
        int d = A*A*A+B-C*C;
        System.out.println("a)ans: "+a);
        System.out.println("b)ans: "+b);
        System.out.println("c)ans: "+c);
        System.out.println("d)ans: "+d);
    }
}
